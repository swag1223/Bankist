/**
 * Handles the hover effect for navigation links.
 *
 * @param {Event} e - The mouseover event object.
 * @param {number} opacity - The desired opacity for the elements during the hover effect.
 *                          Should be a number between 0 (transparent) and 1 (fully opaque).
 */
const handleNavHover = (e, opacity) => {
  if (e.target.classList.contains('nav__link')) {
    const clickedNavLink = e.target;
    const siblings = clickedNavLink
      .closest('.nav')
      .querySelectorAll('.nav__link');
    const logo = clickedNavLink.closest('.nav').querySelector('img');

    siblings.forEach(el => {
      if (el !== clickedNavLink) el.style.opacity = opacity;
    });
    logo.style.opacity = opacity;
  }
};

nav.addEventListener('mouseover', e => {
  handleNavHover(e, 0.5);
});
nav.addEventListener('mouseout', e => {
  handleNavHover(e, 1);
});

