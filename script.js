// Sticky navigation: Intersection Observer API

const header = document.querySelector('.header');
const navHeight = nav.getBoundingClientRect().height;

const stickyNav = entries => {
  const [entry] = entries;
  // console.log(entry);

  if (!entry.isIntersecting) nav.classList.add('sticky');
  else nav.classList.remove('sticky');
};

const headerObserver = new IntersectionObserver(stickyNav, {
  root: null, //viewport only
  threshold: 0,
  rootMargin: `-${navHeight}px`,
});

headerObserver.observe(header);

/////////////////////////////////////////////////////////////////////////////////////////
// REVEAL SECTIONS USING INTERSECTION OBSERVER API

const allSections = document.querySelectorAll('.section');

const revealSections = (entries, observer) => {
  const [entry] = entries;
  //Guard clause
  if (!entry.isIntersecting) return;
  //we are observing all the sections with the same observer so we entry.target helps find the particular section intersecting the viewport , and we make only that section visible not all
  entry.target.classList.remove('section--hidden');
  //unobserve the sections -> once visible because as we scroll more and more events keep adding which is not good
  observer.unobserve(entry.target);
};

const sectionObserver = new IntersectionObserver(revealSections, {
  root: null,
  threshold: 0.15,
});

//attach the observer
allSections.forEach(section => {
  sectionObserver.observe(section);
  //better to add section--hidden class using JS instead of manually adding it in html, because some users might have disabled JS int heir browsers and for them the sections wouldnt be visible to them
  section.classList.add('section--hidden');
});

